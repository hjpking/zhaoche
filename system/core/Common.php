//序列化数组为XML
function & XML_serialize(&$data, $level = 0, $prior_key = NULL)
{
    if ($level == 0) {
        ob_start();
        echo '<?xml version="1.0" ?>', "\n";
    }
    while (list($key, $value) = each($data))
        if (!strpos($key, ' attr')) #if it's not an attribute
            #we don't treat attributes by themselves, so for an empty element
            # that has attributes you still need to set the element to NULL

            if (is_array($value) and array_key_exists(0, $value)) {
                XML_serialize($value, $level, $key);
            } else {
                $tag = $prior_key ? $prior_key : $key;
                echo str_repeat("\t", $level), '<', $tag;
                if (array_key_exists("$key attr", $data)) { #if there's an attribute for this element
                    while (list($attr_name, $attr_value) = each($data["$key attr"]))
                        echo ' ', $attr_name, '="', htmlspecialchars($attr_value), '"';
                    reset($data["$key attr"]);
                }

                if (is_null($value)) echo " />\n";
                elseif (!is_array($value)) echo '>', htmlspecialchars($value), "</$tag>\n"; else echo ">\n", XML_serialize($value, $level + 1), str_repeat("\t", $level), "</$tag>\n";
            }
    reset($data);
    if ($level == 0) {
        $str = & ob_get_contents();
        ob_end_clean();
        return $str;
    }
}

/**
 * 截取字符串
 *
 * @param $str
 * @param int $start
 * @param $length
 * @param string $charset
 * @param string $suffix
 * @return string
 */
function cutStr($str, $start=0, $length, $charset="utf-8", $suffix="")
{
    if(function_exists("mb_substr")){
        return mb_substr($str, $start, $length, $charset).$suffix;
    }
    elseif(function_exists('iconv_substr')){
        return iconv_substr($str,$start,$length,$charset).$suffix;
    }
    $re['utf-8']  = "/[\x01-\x7f]|[\xc2-\xdf][\x80-\xbf]|[\xe0-\xef][\x80-\xbf]{2}|[\xf0-\xff][\x80-\xbf]{3}/";
    $re['gb2312'] = "/[\x01-\x7f]|[\xb0-\xf7][\xa0-\xfe]/";
    $re['gbk']    = "/[\x01-\x7f]|[\x81-\xfe][\x40-\xfe]/";
    $re['big5']   = "/[\x01-\x7f]|[\x81-\xfe]([\x40-\x7e]|\xa1-\xfe])/";
    preg_match_all($re[$charset], $str, $match);
    $slice = join("",array_slice($match[0], $start, $length));
    return $slice.$suffix;
}

/**
 * 解析xml文档
 *
 * @param $xmle
 * @return mixed
 */
function simplexml2array($xmle)
{
    $haschildren = false;
    foreach ($xmle->attributes() as $k => $c) {
        $r[$xmle->getName()]['@att'][$k] = (string)$c;
    }
    foreach ($xmle->children() as $k => $c) {
        $haschildren = true;
        $res = simplexml2array($c);
        if (!empty($res)) {
            $r[$xmle->getName()][] = $res;
        }
    }
    if (!$haschildren) {
        $str = (string)$xmle;
        if (!empty($str)) {
            $r[$xmle->getName()]['@txt'] = (string)$xmle;
        }
    }
    return $r;
}


